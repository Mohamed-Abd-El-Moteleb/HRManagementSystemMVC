using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.Rendering;
using Microsoft.EntityFrameworkCore;
using HRManagementSystem.Models;

namespace HRManagementSystem.Controllers
{
    public class EmployeesController : Controller
    {
        private readonly ApplicationDbContext _context;

        public EmployeesController(ApplicationDbContext context)
        {
            _context = context;
        }

        // GET: Employees
        public async Task<IActionResult> Index(string searchString)
        {
            var employees = _context.Employees
                .Include(e => e.Department)
                .AsQueryable();

            if (!string.IsNullOrEmpty(searchString))
            {
                searchString = searchString.ToLower();

                employees = employees.Where(e =>
                    e.FirstName.ToLower().Contains(searchString) ||
                    e.LastName.ToLower().Contains(searchString) ||
                    e.Email.ToLower().Contains(searchString) ||
                    e.PhoneNumber.ToLower().Contains(searchString) ||
                    e.JobTitle.ToLower().Contains(searchString) ||
                    e.Gender.ToLower().Contains(searchString) ||
                    e.Address.ToLower().Contains(searchString) ||
                    e.NationalId.ToLower().Contains(searchString) ||
                    e.Salary.ToString().Contains(searchString) ||
                    e.HireDate.ToString().Contains(searchString) ||
                    (e.Department != null && e.Department.Name.ToLower().Contains(searchString))
                );
            }

            return View(await employees.Include(e => e.Department).ToListAsync());
        }

